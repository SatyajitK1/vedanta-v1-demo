const fs = require('fs');
const path = require('path');

const EXPORTS_DIR = path.join(__dirname, 'figma-exports');
const OUT_FILE = path.join(__dirname, 'figma-plugin', 'code.js');

const SCREEN_NAMES = [
  'dashboard',
  'analytics-dashboard',
  'request-form',
  'validation-panel',
  'audit-logs',
  'approval-queue',
  'app-shell',
  'design-system-preview',
];

// Load all screen JSON files
const screens = SCREEN_NAMES.map(name => ({
  name,
  layers: JSON.parse(fs.readFileSync(path.join(EXPORTS_DIR, `${name}.json`), 'utf8')),
}));

// Collect unique font families across all screens (tree walk)
const fontFamilies = new Set();
function collectFonts(layers) {
  for (const layer of layers) {
    if (layer.type === 'TEXT' && layer.fontFamily) {
      const first = layer.fontFamily.split(',')[0].trim().replace(/['"]/g, '');
      fontFamilies.add(first);
    }
    if (layer.children) collectFonts(layer.children);
  }
}
for (const screen of screens) collectFonts(screen.layers);
console.log('Fonts found:', [...fontFamilies]);

// Count total layers for logging
function countLayers(layers) {
  let n = layers.length;
  for (const l of layers) if (l.children) n += countLayers(l.children);
  return n;
}

// Generate the plugin code
const pluginCode = `// Auto-generated by build-figma-plugin.js — do not edit manually
const SCREENS = ${JSON.stringify(screens)};

const FONT_MAP = {
  'system-ui': 'Inter',
  '-apple-system': 'Inter',
  'BlinkMacSystemFont': 'Inter',
  'Helvetica Neue': 'Inter',
  'Helvetica': 'Inter',
  'Arial': 'Inter',
  'sans-serif': 'Inter',
  'serif': 'Georgia',
  'monospace': 'Roboto Mono',
  'Segoe UI': 'Inter',
};

function normalizeFontFamily(family) {
  if (!family) return 'Inter';
  const first = family.split(',')[0].trim().replace(/['"]/g, '');
  return FONT_MAP[first] || first;
}

function getSolidFills(fills) {
  if (!fills || !fills.length) return [];
  return fills
    .filter(f => f.type === 'SOLID')
    .map(f => ({
      type: 'SOLID',
      color: { r: f.color.r || 0, g: f.color.g || 0, b: f.color.b || 0 },
      opacity: f.opacity !== undefined ? f.opacity : 1,
      visible: true,
      blendMode: 'NORMAL',
    }));
}

function getEffects(effects) {
  if (!effects || !effects.length) return [];
  return effects.map(e => ({
    type: e.type,
    color: e.color,
    offset: e.offset || { x: 0, y: 0 },
    radius: e.radius || 0,
    spread: e.spread || 0,
    visible: e.visible !== undefined ? e.visible : true,
    blendMode: e.blendMode || 'NORMAL',
  }));
}

function getStrokes(strokes) {
  if (!strokes || !strokes.length) return [];
  return strokes
    .filter(s => s.type === 'SOLID')
    .map(s => ({
      type: 'SOLID',
      color: { r: s.color.r || 0, g: s.color.g || 0, b: s.color.b || 0 },
      opacity: s.opacity !== undefined ? s.opacity : 1,
      visible: true,
      blendMode: 'NORMAL',
    }));
}

// Set of font families successfully loaded
const loadedFonts = new Set();

// layer coords are absolute; parentAbsX/Y is parent's absolute origin
function createLayer(layer, figmaParent, parentAbsX, parentAbsY) {
  try {
    const relX = (layer.x || 0) - parentAbsX;
    const relY = (layer.y || 0) - parentAbsY;
    const w = Math.max(layer.width || 1, 1);
    const h = Math.max(layer.height || 1, 1);

    if (layer.type === 'FRAME') {
      const frame = figma.createFrame();
      frame.name = layer.name || 'Frame';
      frame.x = relX;
      frame.y = relY;
      frame.resize(w, h);
      frame.clipsContent = !!layer.clipsContent;
      const fills = getSolidFills(layer.fills);
      frame.fills = fills.length ? fills : [];
      figmaParent.appendChild(frame);
      if (layer.children) {
        for (const child of layer.children) {
          createLayer(child, frame, layer.x || 0, layer.y || 0);
        }
      }

    } else if (layer.type === 'RECTANGLE') {
      const solidFills = getSolidFills(layer.fills);
      const solidStrokes = getStrokes(layer.strokes);
      const hasEffects = layer.effects && layer.effects.length > 0;
      if (!solidFills.length && !solidStrokes.length && !hasEffects) return;

      const rect = figma.createRectangle();
      rect.x = relX;
      rect.y = relY;
      rect.resize(w, h);
      rect.fills = solidFills;
      if (solidStrokes.length) {
        rect.strokes = solidStrokes;
        rect.strokeWeight = layer.strokeWeight || 1;
        rect.strokeAlign = 'INSIDE';
      }
      if (layer.topLeftRadius) rect.topLeftRadius = layer.topLeftRadius;
      if (layer.topRightRadius) rect.topRightRadius = layer.topRightRadius;
      if (layer.bottomRightRadius) rect.bottomRightRadius = layer.bottomRightRadius;
      if (layer.bottomLeftRadius) rect.bottomLeftRadius = layer.bottomLeftRadius;
      if (hasEffects) rect.effects = getEffects(layer.effects);
      figmaParent.appendChild(rect);

    } else if (layer.type === 'TEXT') {
      const fontFamily = normalizeFontFamily(layer.fontFamily);
      const family = loadedFonts.has(fontFamily) ? fontFamily
                   : loadedFonts.has('Inter') ? 'Inter'
                   : null;
      if (!family) return;

      const text = figma.createText();
      text.fontName = { family, style: 'Regular' };
      if (layer.fontSize) text.fontSize = layer.fontSize;
      if (layer.characters) text.characters = layer.characters;
      text.textAutoResize = 'NONE';
      text.resize(w, h);
      text.x = relX;
      text.y = relY;
      if (layer.fills) text.fills = getSolidFills(layer.fills);
      if (layer.lineHeight) text.lineHeight = layer.lineHeight;
      if (layer.letterSpacing) text.letterSpacing = layer.letterSpacing;
      if (layer.textAlignHorizontal) text.textAlignHorizontal = layer.textAlignHorizontal;
      if (layer.textCase) text.textCase = layer.textCase;
      if (layer.textDecoration) text.textDecoration = layer.textDecoration;
      figmaParent.appendChild(text);

    } else if (layer.type === 'SVG') {
      try {
        const svgNode = figma.createNodeFromSvg(layer.svg);
        svgNode.x = relX;
        svgNode.y = relY;
        figmaParent.appendChild(svgNode);
      } catch (e) {
        const rect = figma.createRectangle();
        rect.x = relX;
        rect.y = relY;
        rect.resize(w, h);
        rect.fills = [{ type: 'SOLID', color: { r: 0.85, g: 0.85, b: 0.85 }, opacity: 1, visible: true, blendMode: 'NORMAL' }];
        figmaParent.appendChild(rect);
      }
    }
  } catch (e) {
    console.error('Layer error [' + layer.type + ']:', e.message);
  }
}

(async () => {
  // Pre-load all fonts used across all screens
  const allFamilies = new Set(['Inter']);
  function collectFamilies(layers) {
    for (const layer of layers) {
      if (layer.type === 'TEXT' && layer.fontFamily) {
        allFamilies.add(normalizeFontFamily(layer.fontFamily));
      }
      if (layer.children) collectFamilies(layer.children);
    }
  }
  for (const screen of SCREENS) collectFamilies(screen.layers);

  for (const family of allFamilies) {
    try {
      await figma.loadFontAsync({ family, style: 'Regular' });
      loadedFonts.add(family);
    } catch (e) {
      console.warn('Font not available: ' + family);
    }
  }
  console.log('Fonts loaded: ' + [...loadedFonts].join(', '));

  const SPACING = 120;
  let offsetX = 0;

  for (const screen of SCREENS) {
    try {
      // useFrames=true returns [rootFrame] — one element with nested children
      const rootLayer = screen.layers[0];

      const frame = figma.createFrame();
      frame.name = screen.name
        .replace(/-/g, ' ')
        .replace(/\\b\\w/g, c => c.toUpperCase());
      frame.resize(rootLayer.width || 1440, rootLayer.height || 900);
      frame.x = offsetX;
      frame.y = 0;
      frame.clipsContent = true;
      frame.fills = [{ type: 'SOLID', color: { r: 0.96, g: 0.96, b: 0.97 }, opacity: 1, visible: true, blendMode: 'NORMAL' }];
      figma.currentPage.appendChild(frame);

      if (rootLayer.children) {
        for (const child of rootLayer.children) {
          createLayer(child, frame, rootLayer.x || 0, rootLayer.y || 0);
        }
      }

      offsetX += (rootLayer.width || 1440) + SPACING;
      const childCount = rootLayer.children ? rootLayer.children.length : 0;
      console.log('Created: ' + frame.name + ' (' + childCount + ' top-level children)');
    } catch (e) {
      console.error('Screen error [' + screen.name + ']:', e.message);
    }
  }

  figma.viewport.scrollAndZoomIntoView(figma.currentPage.children);
  figma.closePlugin('Done! ' + SCREENS.length + ' screens imported into Figma.');
})();
`;

fs.writeFileSync(OUT_FILE, pluginCode);
console.log(`\nPlugin built: ${OUT_FILE}`);
console.log(`Total screens: ${screens.length}`);
console.log(`Total layers: ${screens.reduce((sum, s) => sum + countLayers(s.layers), 0)}`);
